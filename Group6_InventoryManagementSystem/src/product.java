    private void addPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPActionPerformed

        // TODO add your handling code here:

        try{
            st=cn.createStatement();
            String ProductId = productid.getText();
            String ProductName = productname.getText();
            String Description = description.getText();
            String Barcode = barcode.getText();
            int Price = Integer.parseInt(price.getText());
            int Quantity = Integer.parseInt(quantity.getText());
            String Category = (String) category.getSelectedItem();

            String query = "INSERT INTO product (productId,ProductName,Description,Barcode,Price,Quantity,Category) VALUES(?,?,?,?,?,?,?)";
            PreparedStatement preparedSt = cn.prepareStatement(query);
            preparedSt.setString(1, ProductId);
            preparedSt.setString(2, ProductName);
            preparedSt.setString(3, Description);
            preparedSt.setString(4, Barcode);
            preparedSt.setInt(5, Price);
            preparedSt.setInt(6, Quantity);
            preparedSt.setString(7, Category);

            preparedSt.execute();

            JOptionPane.showMessageDialog(null, "Added Successfully");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Not Added. Please enter correct id or datatype");
        }
    }//GEN-LAST:event_addPActionPerformed

