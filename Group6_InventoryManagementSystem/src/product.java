    private void editPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editPActionPerformed
        // TODO add your handling code here:
        try{
            String query = "UPDATE product SET ProductName=?,Description=?,Barcode=?,Price=?,Quantity=?,Category=? WHERE productId=?";
            pst = cn.prepareStatement(query);
            pst.setString(7, productid.getText());
            pst.setString(1, productname.getText());
            pst.setString(2, description.getText());
            pst.setString(3, barcode.getText());
            pst.setString(4, price.getText());
            pst.setString(5, quantity.getText());
            pst.setString(6, (String) category.getSelectedItem());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Updated successfully");

           }catch(SQLException | HeadlessException ex){
            JOptionPane.showMessageDialog(null, ex); 
           }
    }//GEN-LAST:event_editPActionPerformed

